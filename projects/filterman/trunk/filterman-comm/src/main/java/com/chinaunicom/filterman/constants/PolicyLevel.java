package com.chinaunicom.filterman.constants;

public enum PolicyLevel {
	nonePolicyLevel("无防护策略", 0), lowPolicyLevel("低防护策略", 1), 
	middlePolicyLevel("中防护策略", 2), highPolicyLevel("高防护策略", 3);

	private String name;
	private int id;

	private PolicyLevel(String name, int id) {
		this.name = name;
		this.id = id;
	}

	public String getPolicyLevelName() {
		return this.name;
	}

	public int getPolicyLevelId() {
		return this.id;
	}
}
